a = int(input('Digite um número \n'))
b = int(input('Digite outro número \n'))
mdc = 1
d = 2
adiv = a/d
bdiv = b/d
while adiv>1 and bdiv>1:
    while a%d==0 and b%d==0:
        adiv and bdiv
        mdc = mdc*d
    while a%d!=0 and b%d==0:
        bdiv
    while a%d==0 and b%d!=0:
        adiv
d+=d

print (mdc)